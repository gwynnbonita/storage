  USER_ID CHAR(4) NOT NULL,
  PROFILE_ID CHAR(1) NOT NULL,
  PROFILE_NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (PROFILE_ID),
  FOREIGN KEY (USER_ID) REFERENCES USERACCOUNT(USER_ID)
